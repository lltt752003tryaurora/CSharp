using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Text.RegularExpressions;
using System.Threading.Tasks;

namespace Utilities
{
    public class StringHelper
    {
        public static string Base64Encode(string plainText)
        {
            var plainTextBytes = System.Text.Encoding.UTF8.GetBytes(plainText);
            return System.Convert.ToBase64String(plainTextBytes);
        }
        public static string Base64Decode(string base64EncodedData)
        {
            var base64EncodedBytes = System.Convert.FromBase64String(base64EncodedData);
            return System.Text.Encoding.UTF8.GetString(base64EncodedBytes);
        }
        public static string RemoveVNSign(string text)
        {
            string[] arr1 = new string[] {
                "á", "à", "ả", "ã", "ạ", "â", "ấ", "ầ", "ẩ", "ẫ", "ậ", "ă", "ắ", "ằ", "ẳ", "ẵ", "ặ",
                "đ",
                "é","è","ẻ","ẽ","ẹ","ê","ế","ề","ể","ễ","ệ",
                "í","ì","ỉ","ĩ","ị",
                "ó","ò","ỏ","õ","ọ","ô","ố","ồ","ổ","ỗ","ộ","ơ","ớ","ờ","ở","ỡ","ợ",
                "ú","ù","ủ","ũ","ụ","ư","ứ","ừ","ử","ữ","ự",
                "ý","ỳ","ỷ","ỹ","ỵ"
            };
            string[] arr2 = new string[] { "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a",
                "d",
                "e","e","e","e","e","e","e","e","e","e","e",
                "i","i","i","i","i",
                "o","o","o","o","o","o","o","o","o","o","o","o","o","o","o","o","o",
                "u","u","u","u","u","u","u","u","u","u","u",
                "y","y","y","y","y"
            };
            for (int i = 0; i < arr1.Length; i++)
            {
                text = text.Replace(arr1[i], arr2[i]);
                text = text.Replace(arr1[i].ToUpper(), arr2[i].ToUpper());
            }
            return text;
        }
        public static string ReplaceSecialChar(string input, string replacement)
        {
            input = RemoveVNSign(input);
            return Regex.Replace(input, "[^\\w\\._]", replacement);
        }
        public static string DicToQueryString(Dictionary<string, object> dic)
        {
            StringBuilder sb = new StringBuilder();
            foreach (KeyValuePair<string, object> kv in dic)
            {
                sb.AppendFormat("{0}={1}&", kv.Key, kv.Value.ToString());
            }
            return sb.ToString().TrimEnd('&');
        }
        public static Dictionary<string,object> QueryStringToDic(string queryString)
        {
            Dictionary<string, object> dic = new Dictionary<string, object>();
            queryString = queryString.TrimStart('&').TrimEnd('&');
            string []qrs = queryString.Split('&');
            foreach (string s in qrs)
            {
                List<string> items = s.Split('=').ToList();
                dic.Add(items[0], items[1]);
            }
            return dic;
        }

    }
